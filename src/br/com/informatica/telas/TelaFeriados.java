    private void pesquisaFeridado() {
        String sql = "select * from feriados where descricaoFeriado LIKE ? order by dataFeriado desc";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, "%" + jTextFieldBuscaFeriado.getText() + "%");
            rs = pst.executeQuery();
            jTableFeriado.setModel(DbUtils.resultSetToTableModel(rs));
            jTableFeriado.isValid();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void setarCampos() {
        int setar = jTableFeriado.getSelectedRow();
        jTextiDFeriado.setText(jTableFeriado.getModel().getValueAt(setar, 0).toString());
        String dataFeriado = (jTableFeriado.getModel().getValueAt(setar, 1).toString());
        try {
            Date feriadoDate = new SimpleDateFormat("yyyy-MM-dd").parse(dataFeriado);
            jDateDataFeriado.setDate(feriadoDate);
        } catch (ParseException ex) {
            Logger.getLogger(TelaSaida.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTextFieldDescricao.setText(jTableFeriado.getModel().getValueAt(setar, 2).toString());
      

    }

    private void cadastrarFeriado() {
        String sql = "INSERT INTO feriados (dataFeriado,descricaoFeriado,anoFeriado ) VALUES (?, ?, ?)";
        try {

            SimpleDateFormat sdf = new SimpleDateFormat("yyyyMMdd");

            pst = conexao.prepareStatement(sql);//---- 13091968

            String dataInserNoBanco = "";
            SimpleDateFormat formato = new SimpleDateFormat("yyyyMMdd");
            java.util.Date recebFormato = jDateDataFeriado.getDate();
            dataInserNoBanco = formato.format(recebFormato);
            String anoInserido = dataInserNoBanco.substring(0, 4);
            System.out.println("anoInserido: " + anoInserido);
            System.out.println("###################//######################");

            String mesInserido = dataInserNoBanco.substring(4, 6);
            String diaInserido = dataInserNoBanco.substring(6);

            //inseri a data no formato do
            String dataMysql = anoInserido + "-" + mesInserido + "-" + diaInserido;
            System.out.println("DataMysql : " + dataMysql);
            pst.setString(1, dataMysql);
            pst.setString(2, jTextFieldDescricao.getText().toUpperCase());
            pst.setString(3, anoInserido);

            // validaçao dos campos obrigatórios
            if (jTextFieldDescricao.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo Descrição é Obrigatório!");

            } else {
                int rowsAfected = pst.executeUpdate();
                //  palestra de informatica
                if (rowsAfected > 0) {
                    JOptionPane.showMessageDialog(null, "Feridado cadastrado com sucesso!", "Feriado", 1);
                    jTextFieldDescricao.setText(null);
                    jDateDataFeriado.setDate(null);

                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaSaida.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

