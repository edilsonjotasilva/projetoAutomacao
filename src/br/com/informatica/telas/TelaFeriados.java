    private void excluirFeriado() {
      int confirma = JOptionPane.showConfirmDialog(null, "Confirma a remoção do Feriado ? " + jTextFieldDescricao.getText(), "Atenção", JOptionPane.YES_NO_OPTION);
        //se confirmar for = YES_OPTION, o comando sql será executado, se txtUsuNom.getText(),
        //for Empty significa que não ha usuario com esse ID
        if (confirma == JOptionPane.YES_OPTION) {

            String sql = "DELETE FROM feriados WHERE id = ?";
            try {
                pst = conexao.prepareStatement(sql);
                pst.setString(1, jTextiDFeriado.getText());
                int rowsAffected = pst.executeUpdate();
               
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(null, "Feriado Removido com Sucesso!");
                    jTextiDFeriado.setText(null);
                    jDateDataFeriado.setDate(null);
                    jTextFieldDescricao.setText(null);
                    btnCadastrarFeriado.setEnabled(true);
                    jTextFieldBuscaFeriado.requestFocus();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }

