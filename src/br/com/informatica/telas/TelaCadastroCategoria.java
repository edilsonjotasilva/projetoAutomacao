    private void adicionarCategoria() {
        try {
                      // "INSERT INTO saida (dataSaida,descSaida,valorSaida,categoria_codCategoria) VALUES (?, ?, ?, ?)";
            String sql = "INSERT INTO categoria(codCategoria,descricao,operacao) VALUES(?, ?, ?)";
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtCadastraCategoria.getText());
            pst.setString(2, txtDescCategoria.getText());
            pst.setString(3, txtOperacaoCategoria.getText());
            if (txtCadastraCategoria.getText().isEmpty()) {
                        JOptionPane.showMessageDialog(null, "Campo Cod Categoria é Obrigatório!");
                    } else if (txtDescCategoria.getText().isEmpty()) {
                        JOptionPane.showMessageDialog(null, "Campo Descricão é Obrigatório!");
                    } else if (txtOperacaoCategoria.getText().isEmpty()) {
                        JOptionPane.showMessageDialog(null, "Campo Operação  é Obrigatório!");
                    } else {
                         int rowsAfected = pst.executeUpdate();

                        if (rowsAfected > 0) {
                            JOptionPane.showMessageDialog(null, "Saida cadastrada com sucesso!", "Categoria", 1);
                            txtCadastraCategoria.setText(null);
                            txtDescCategoria.setText(null);
                            txtOperacaoCategoria.setText(null);
                           

                        }
                    }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
