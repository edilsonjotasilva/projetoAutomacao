    private void pesquisarImovel() {
            String sql = "select * from imovel";
        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();
            tblImovel.setModel(DbUtils.resultSetToTableModel(rs));
            tblImovel.isValid();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

