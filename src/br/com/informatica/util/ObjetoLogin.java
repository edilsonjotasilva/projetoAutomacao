package br.com.informatica.util;

public class ObjetoLogin {
    private String login, senha, perfi;

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public String getPerfi() {
        return perfi;
    }

    public void setPerfi(String perfi) {
        this.perfi = perfi;
    }
    
}
