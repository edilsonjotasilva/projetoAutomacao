   private void pesquisarBoleto() {
        String sql = "select codBoleto,status,CN,dataVenc,valorIntegral,quantPrest,pagador,cpf,rua,quadra,lote,loteamento,cidade from boleto where pagador LIKE ?";
        GerarBoleto gerarBoleto = new GerarBoleto();
        try {

         //  String pesquisaBoleto = gerarBoleto.jTextPesqPagador.getText();
            pst = conexao.prepareStatement(sql);
         
           pst.setString(1, "%"+txtPesquisaBoleto.getText()+"%");
        //    pst.setString(1, "%"+pesquisaBoleto+"%");
            rs = pst.executeQuery();
            jTableBoleto.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
