    private void pesquisarBoleto() {
        String sql = "select codBoleto,status,dataVenc,quantPrest,pagador,cpf,rua,quadra,lote,loteamento,cidade from boleto where pagador LIKE ? order by dataVenc";

        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, "%" + txtPesquisaBoleto.getText() + "%");
            rs = pst.executeQuery();
            jTableBoleto.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
